create table person(
	id int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	fullName varchar(150) NOT NULL UNIQUE,
	year int NOT NULL	
)

create table books(
	id int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	title varchar(300) NOT NULL,
	author varchar(150) NOT NULL,
	year int NOT NULL,
	person_id int REFERENCES person(id) ON DELETE SET NULL,
	take_date Date
)